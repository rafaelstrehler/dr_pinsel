		Dr. François Forel aus Chigny sur Morges initiierte im Jahr 1947 den 
		Verein. Mit einem Rundschreiben gelangte er an Kolleginnen und Kollegen 
		und konnte so innerhalb kurzer Zeit eine ansehnliche Anzahl Mitglieder 
		gewinnen. Am 18.12.48 fanden die Gründungsversammlung und dann die erste, 
		damals vielbeachtete Ausstellung in Lausanne statt.<br><br>
		
		Die zweite Vernissage wurde im November 1949 in Bern durchgeführt, und 
		die eingesandten Werke wurden vorgängig von einer dreiköpfigen Jury 
		gesiebt, sehr zum Ärger der zurückgewiesenen Ärztekünstler. Nach heftigen 
		Protesten an der nächsten Geschäftssitzung drohte dem noch jungen Verein 
		bereits das Aus. Nach vielen Briefwechseln fand man sich aber wieder und 
		raufte sich zusammen. Zur nächsten Ausstellung wurden wieder alle 
		eingereichten Werke zugelassen, eine Jury wurde nicht mehr bemüht.<br><br>
		
		In den folgenden Jahren fand die Ausstellung in Universitäten, Galerien, 
		Schlössern und Museen in der ganzen Schweiz statt. In Zusammenarbeit mit 
		der internationalen Vereinigung malender Ärzte konnte sich der Verein 
		sogar an Ausstellungen in Paris, Turin und Tokio präsentieren. Seit 
		1990 ist die Ausstellung im ehrwürdigen Rathauskeller im malerischen 
		Murten (FR) sesshaft geworden.<br><br>
		
		Die Ausstellung in Murten findet meist zwischen Mitte Juli und Mitte 
		August statt.<br><br>
		
